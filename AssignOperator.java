package Assignment;

public class AssignOperator {
	public static void main(String args[]) {
		int a=10,b=9,c=5,d=20,e;
		a+=5;
		System.out.println(a);
		b-=5;
		System.out.println(b);
		c*=5;
		System.out.println(c);
		d/=5;
		System.out.println(d);
		e=a+b+c+d;
		System.out.println(e);
		
	}

}
