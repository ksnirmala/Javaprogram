package compare;

public class ComOperator {
	public static void main(String args[]) {
		int a=10,b=6;
		boolean c;
		c=(a>b);
		System.out.println(c);
		c=(a<b);
		System.out.println(c);
		c=(a>=b);
		System.out.println(c);
		c=(a<=b);
		System.out.println(c);
		c=(a==b);
		System.out.println(c);
		
		
	}

}
